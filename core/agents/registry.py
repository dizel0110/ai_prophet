def get_all_agents() -> list:
    """Создать экземпляры AgentBase для всех агентов, кроме final_expert (он отдельно)."""
    agents = []
    for group_key, group in AGENT_GROUPS.items():
        for a in group["agents"]:
            if a["id"] == "final_expert":
                continue
            agents.append(AgentBase(a["id"], a["name"], a["role"], a.get("model_type", "text")))
    return agents


def get_agent_def(agent_id: str) -> dict:
    """Получить определение агента по его id."""
    for group_key, group in AGENT_GROUPS.items():
        for a in group["agents"]:
            if a["id"] == agent_id:
                return a
    return {}


def get_group_by_agent(agent_id: str) -> str:
    """Вернуть ключ группы, в которой находится агент."""
    for group_key, group in AGENT_GROUPS.items():
        for a in group["agents"]:
            if a["id"] == agent_id:
                return group_key
    return ""


def get_final_expert_def() -> dict:
    """Получить определение финального эксперта."""
    return get_agent_def("final_expert")


def get_groups_summary() -> str:
    """Текстовое описание всех групп и агентов для вывода пользователю."""
    lines = ["🧑‍⚕️ *Команда ИИ-специалистов:*\n"]
    for group_key, group in AGENT_GROUPS.items():
        lines.append(f"**{group['name']}** — {group['description']}\n")
        for a in group["agents"]:
            lines.append(f"  • *{a['name']}* ← {a['input']}")
        lines.append("")
    return "\n".join(lines)
